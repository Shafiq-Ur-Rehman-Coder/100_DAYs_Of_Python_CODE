# Printing a simple string
print("Hello, World!")          # Output: Hello, World!

# Printing a number
print(10)                       # Output: 10

print(10+10)                    # Output: 20

print(10*10)                    # Output: 100 ||| Whatever you do /*- plus minus percentage | The Output will be correct

# Printing a float number
print(3.14)                     # Output: 3.14

# Single-line comment example
print("Python")                 # Output: Python

# Printing using single quotes
print('Hello Python')           # Output: Hello Python

# Printing using double quotes
print("Hello Python")           # Output: Hello Python

